    private void cargarDatosPais() {
    // Definir columnas explícitamente
    DefaultTableModel Modelo = new DefaultTableModel(
            new Object[]{"Código", "Nombre", "Continente", "Población", "Tipo Gobierno"}, 0
    );

    // Obtener filtros si los tuvieras (ejemplo: txtNombrePais, etc.)
    String codigo = txtCodigo.getText();
    String nombre = txtNombre.getText();
    String continente = cboxContinente.getSelectedItem() != null 
        ? cboxContinente.getSelectedItem().toString() 
        : "";

    String poblacion = txtPoblacion.getText();
    boolean tipoGobierno = chkTipoGobierno.isSelected();


    // Uso de DAO/Control
    Controles.PaisControl dao = new Controles.PaisControl();
    java.util.List<Modelo.Pais> lista = dao.listarPaises(codigo, nombre, continente, poblacion, tipoGobierno);

    // Llenar el modelo con objetos
    for (Modelo.Pais p : lista) {
        Modelo.addRow(new Object[]{
                p.getCodigoPais(),
                p.getNombrePais(),
                p.getContinentePais(),
                p.getPoblacionPais(),
                p.isTipoGobierno() ? "Democracia" : "Otro"
        });
    }

    // Mensajes de control
    if (lista.isEmpty()) {
        System.out.println("No se encontraron países.");
    } else {
        System.out.println(lista.size() + " países cargados.");
    }

    // Asignar modelo a la tabla
    jTablePais.setModel(Modelo);
}

    
    private void cargarDatosCiudad() {
    DefaultTableModel model = (DefaultTableModel) jTableCiudades.getModel();
    model.setRowCount(0);

    String sql = "SELECT nombreCiudad, poblacionCiudad FROM Ciudad";

    try (Connection conn = Conexion.ConexionBD.conectar();
         PreparedStatement ps = conn.prepareStatement(sql);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            String nombreCiudad = rs.getString("nombreCiudad");
            
            int poblacion = rs.getInt("poblacionCiudad"); 
            
            Object[] fila = {
                nombreCiudad,
                poblacion,
            };

            model.addRow(fila);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al cargar datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    private void cargarDatosIdiomas() {
    DefaultTableModel model = (DefaultTableModel) jTableCiudades1.getModel();
    model.setRowCount(0);

    String sql = "SELECT nombreIdioma, oficial FROM idioma";

    try (Connection conn = Conexion.ConexionBD.conectar();
         PreparedStatement ps = conn.prepareStatement(sql);
         ResultSet rs = ps.executeQuery()) {

        while (rs.next()) {
            String idioma = rs.getString("nombreIdioma");
            
            int oficialDB = rs.getInt("oficial"); 

            String esOficialTexto = (oficialDB == 1) ? "Sí" : "No";

            Object[] fila = {
                idioma,        
                esOficialTexto
            };

            model.addRow(fila);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al cargar datos de idiomas: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    

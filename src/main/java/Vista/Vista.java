    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
try {
    DefaultTableModel model = (DefaultTableModel) jTablePais.getModel();

    String codigo = txtCodigo.getText().trim();
    String nombre = txtNombre.getText().trim();
    String continente = cboxContinente.getSelectedItem().toString();
    int poblacion = Integer.parseInt(txtPoblacion.getText().trim());
    boolean esDemocracia = chkTipoGobierno.isSelected();

    if (codigo.isEmpty() || nombre.isEmpty() || continente.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor completa todos los campos obligatorios.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Object[] newRow = {codigo, nombre, continente, poblacion, esDemocracia ? "Democracia" : "Otro"};
    model.addRow(newRow);

    try (Connection conn = Conexion.ConexionBD.conectar()) {
        String sql = "INSERT INTO Pais (codigoPais, nombrePais, continentePais, poblacionPais, tipoGobierno) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, codigo);
        ps.setString(2, nombre);
        ps.setString(3, continente);
        ps.setInt(4, poblacion);
        ps.setBoolean(5, esDemocracia);

        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "País agregado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos: " + ex.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
    }

    txtCodigo.setText("");
    txtNombre.setText("");
    txtPoblacion.setText("");
    cboxContinente.setSelectedIndex(0);
    chkTipoGobierno.setSelected(false);

} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Error: La población debe ser un número válido.", "Dato inválido", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    try {
        DefaultTableModel model = (DefaultTableModel) jTablePais.getModel();

      
        String codigo = txtCodigo.getText().trim(); // clave primaria
        String nombre = txtNombre.getText().trim();
        String continente = cboxContinente.getSelectedItem().toString();
        int poblacion = Integer.parseInt(txtPoblacion.getText().trim());
        boolean esDemocracia = chkTipoGobierno.isSelected();

        if (codigo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar el código del país a modificar.", "Código requerido", JOptionPane.WARNING_MESSAGE);
            return;
        }

      
        try (Connection conn = Conexion.ConexionBD.conectar()) {
            String sql = "UPDATE Pais SET nombrePais = ?, continentePais = ?, poblacionPais = ?, tipoGobierno = ? WHERE codigoPais = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nombre);
            ps.setString(2, continente);
            ps.setInt(3, poblacion);
            ps.setBoolean(4, esDemocracia);
            ps.setString(5, codigo);

            int filasAfectadas = ps.executeUpdate();

            if (filasAfectadas > 0) {
                JOptionPane.showMessageDialog(this, "País modificado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

           
                int filaSeleccionada = jTablePais.getSelectedRow();
                if (filaSeleccionada != -1) {
                    model.setValueAt(codigo, filaSeleccionada, 0);
                    model.setValueAt(nombre, filaSeleccionada, 1);
                    model.setValueAt(continente, filaSeleccionada, 2);
                    model.setValueAt(poblacion, filaSeleccionada, 3);
                    model.setValueAt(esDemocracia ? "Democracia" : "Otro", filaSeleccionada, 4);
                }
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró un país con ese código.", "Sin resultados", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al modificar en la base de datos: " + ex.getMessage(), "Error BD", JOptionPane.ERROR_MESSAGE);
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error: La población debe ser un número válido.", "Dato inválido", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnModificarActionPerformed

